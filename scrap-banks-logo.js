const axios = require('axios');
const fs = require('fs');
const path = require('path');

// Массив ссылок на изображения
const imageUrls = logos();

// Функция для загрузки изображения
async function downloadImage(url, filename) {
    try {
        const response = await axios({
            url,
            method: 'GET',
            responseType: 'stream'
        });

        const writer = fs.createWriteStream('src/img/bank-logos/'+filename);

        response.data.pipe(writer);

        return new Promise((resolve, reject) => {
            writer.on('finish', resolve);
            writer.on('error', reject);
        });
    } catch (error) {
        console.error(`Ошибка при загрузке изображения ${url}:`, error);
    }
}

// Основная функция для загрузки всех изображений
async function downloadAllImages(urls) {
    for (const url of urls) {
        const filename = path.basename(url);
        await downloadImage(url, filename);
        console.log(`Изображение ${filename} загружено.`);
    }
}

// Запуск скрипта
downloadAllImages(imageUrls)
    .then(() => console.log('Все изображения загружены.'))
    .catch(error => console.error('Ошибка:', error));


